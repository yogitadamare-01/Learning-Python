#STRING METHODS
name="yogita"

#upper
print(name.upper())

#lower
print(name.lower())

#rstrip
a="hurry uppp !!!!!!!"
print(a.rstrip("!"))

#replace
print(name.replace("yogita","vivek"))

#split
b="my name is yogita"
print(b.split())

#capitalize
print(b.capitalize())

#center
print(b.center(50))
print(len(b.center(50)))

#count
c="yogita and yogita,yogita"
print(c.count("yogita"))

#endswith
print(b.endswith("ita"))
print(b.endswith("is",4,10))

#find
print(b.find("is"))

#index
print(b.index("s"))

#isalnum
d="alpha2345"
print(d.isalnum())

#isalpha
print(d.isalpha())

#islower
print(b.islower())

#isprintable
e="yogita\n"
print(e.isprintable())

#isspace
f="  "
print(f.isspace())

#istitle
str1="World Health Organizaation"
print(str1.istitle())

#isupper
name1="Harry"
print(name1.isupper())

#startswith
print(name1.startswith("ha"))

#swapcase
print(name1.swapcase())

#title
print(b.title())



































